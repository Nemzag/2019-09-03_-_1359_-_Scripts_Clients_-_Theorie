							<code class="language-javascript">.textContent</code> returns every element in the node.<br>
							In contrast, <code class="language-javascript">.innerText</code> is aware of styling and won’t return the text of “hidden” elements.<br>
							Moreover, since <code class="language-javascript">.innerText</code> takes CSS styles into account, reading the value of <code class="language-javascript">.innerText</code> triggers a reflow to ensure up-to-date computed styles. (Reflows can be computationally expensive, and thus should be avoided when possible.)<br>Unlike <code class="language-javascript">.textContent</code>, altering <code class="language-javascript">.innerText</code> in Internet Explorer (version 11 and below) removes child nodes from the element and permanently destroys all descendant text nodes. It is impossible to insert the nodes again into any other element or into the same element anymore.
