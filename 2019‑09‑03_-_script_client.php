				<p>Don't get confused by the differences between Node.textContent and HTMLElement.innerText. Although the names seem similar, there are important differences:</p>

				<p>textContent gets the content of all elements, including &lt;script> and &lt;style> elements. In contrast, innerText only shows “human-readable” elements.<br>
                  textContent returns every element in the node. In contrast, innerText is aware of styling and won’t return the text of “hidden” elements. Moreover, since innerText takes CSS styles into account, reading the value of innerText triggers a reflow to ensure up-to-date computed styles. (Reflows can be computationally expensive, and thus should be avoided when possible.)<br>
              Unlike textContent, altering innerText in Internet Explorer (version 11 and below) removes child nodes from the element and permanently destroys all descendant text nodes. It is impossible to insert the nodes again into any other element or into the same element anymore.</p>

				<h4>Differences from innerHTML</h4>

              <p>Element.innerHTML returns HTML, as its name indicates. Sometimes people use innerHTML to retrieve or write text inside an element, but textContent has better performance because its value is not parsed as HTML. Moreover, using textContent can prevent XSS attacks.</p>

