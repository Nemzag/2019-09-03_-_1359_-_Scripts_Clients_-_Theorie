				  <h3 id="titleH3DoWhile"><a href="?#title-h2-table-of-contents">do … while</a></h3>

				  <p>L'instruction <code>do...while</code>,<br>
					crée une boucle qui exécute une instruction jusqu'à ce qu'une condition de test ne soit plus vérifiée.<br>
					La condition est testée après que l'instruction soit exécutée,<br>
					l'instruction définie dans la boucle est donc exécutée au moins une fois.</p>

				  <pre><code class="language-javascript line-numbers">var result = "";
var i = 0;

do {
  i = i + 1;
  result = result + i;
} while (i < 5);

console.log(result);
// expected result: "12345"
</code></pre>

				  <h4 id="titleH4DoWhileSyntaxe">Syntaxe</h4>

				  <pre><code class="language-javascript line-numbers">do
   <em>instruction</em>
while (<em>condition</em>);
</code></pre>

				  <dl>
					<dt><code>instruction</code></dt>
					<dd>Une instruction exécutée au moins une fois et ré-exécutée chaque fois que la condition de test est évaluée à <code>true</code>. On peut exécuter plusieurs instructions au sein d'une boucle grâce à l'instruction <a class="new" href="/fr/docs/Web/JavaScript/Reference/Instructions/block" rel="nofollow" title="Cette documentation n'a pas encore été rédigée, vous pouvez aider en contribuant&nbsp;!"><code>block</code></a> (<code>{ ... }</code>) qui permet de grouper différentes instructions en une seule.</dd>
				  </dl>

				  <dl>
					<dt><code>condition</code></dt>
					<dd>Une expression évaluée après chaque passage dans la boucle. Si l'évaluation de la <code>condition</code> donne <code>true</code> (la condition est vérifiée), <code>instruction</code> sera exécutée à nouveau. Lorsque <code>condition</code> donne <code>false</code>, le contrôle passe à l'instruction suivant la boucle <code>do...while</code>.</dd>

